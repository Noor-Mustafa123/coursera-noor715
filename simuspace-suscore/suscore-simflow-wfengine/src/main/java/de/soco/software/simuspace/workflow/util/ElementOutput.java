package de.soco.software.simuspace.workflow.util;

/**
 * The Class ElementOutput.
 */
public class ElementOutput {

    /**
     * The exit code.
     */
    protected String exitCode;

    /**
     * Gets the exit code.
     *
     * @return the exit code
     */
    public String getExitCode() {
        return exitCode;
    }

    /**
     * Sets the exit code.
     *
     * @param exitCode
     *         the new exit code
     */
    public void setExitCode( String exitCode ) {
        this.exitCode = exitCode;
    }

}
