package de.soco.software.simuspace.suscore.common.constants;

/**
 * The Class ConstantRequestHeader is responsible to provide some HTTP Headers keys..
 *
 * @author Ahsan Khan
 */
public class ConstantRequestHeader {

    /**
     * The request header accept.
     */
    public static final String ACCEPT = "Accept";

    /**
     * The value of header CONTENT_TYPE.
     */
    public static final String APPLICATION_JSON = "application/json";

    /**
     * The request header authentication token.
     */
    public static final String AUTH_TOKEN = "X-Auth-Token";

    public static final String JOB_TOKEN = "X-Job-Token";

    /**
     * The Constant BASE_URL.
     */
    public static final String BASE_URL = "/api/";

    /**
     * The request header content type.
     */
    public static final String CONTENT_TYPE = "Content-Type";

    /**
     * The Constant FILE.
     */
    public static final String FILE = "file";

    /**
     * The Constant URL.
     */
    public static final String URL = "url";

    /**
     * The Constant AUTHORIZATION.
     */
    public static final String AUTHORIZATION = "Authorization";

    /**
     * The Constant DEST_PATH.
     */
    public static final String DEST_PATH = "Dest-path";

    /**
     * The Constant USER_ID.
     */
    public static final String USER_ID = "UserId";

    /**
     * The Constant USER_UID.
     */
    public static final String USER_UID = "UserUid";

}
