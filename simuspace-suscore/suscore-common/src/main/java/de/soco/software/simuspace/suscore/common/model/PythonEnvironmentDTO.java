package de.soco.software.simuspace.suscore.common.model;

/**
 * The type Python environment dto.
 */
public class PythonEnvironmentDTO {

    /**
     * The Name.
     */
    private String name;

    /**
     * The Path.
     */
    private String path;

    /**
     * Gets name.
     *
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * Sets name.
     *
     * @param name
     *         the name
     */
    public void setName( String name ) {
        this.name = name;
    }

    /**
     * Gets path.
     *
     * @return the path
     */
    public String getPath() {
        return path;
    }

    /**
     * Sets path.
     *
     * @param path
     *         the path
     */
    public void setPath( String path ) {
        this.path = path;
    }

}
