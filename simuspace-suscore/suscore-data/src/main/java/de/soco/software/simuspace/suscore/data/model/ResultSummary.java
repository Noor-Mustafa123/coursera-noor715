package de.soco.software.simuspace.suscore.data.model;

/**
 * The Class ResultSummary.
 */
public class ResultSummary {

    /**
     * The exp num.
     */
    private String expNum;

    /**
     * The design.
     */
    private String design;

    /**
     * The result.
     */
    private String result;

    /**
     * Gets the exp num.
     *
     * @return the exp num
     */
    public String getExpNum() {
        return expNum;
    }

    /**
     * Sets the exp num.
     *
     * @param expNum
     *         the new exp num
     */
    public void setExpNum( String expNum ) {
        this.expNum = expNum;
    }

    /**
     * Gets the design.
     *
     * @return the design
     */
    public String getDesign() {
        return design;
    }

    /**
     * Sets the design.
     *
     * @param design
     *         the new design
     */
    public void setDesign( String design ) {
        this.design = design;
    }

    /**
     * Gets the result.
     *
     * @return the result
     */
    public String getResult() {
        return result;
    }

    /**
     * Sets the result.
     *
     * @param result
     *         the new result
     */
    public void setResult( String result ) {
        this.result = result;
    }

}
